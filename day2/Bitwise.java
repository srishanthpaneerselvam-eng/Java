package day2;

public class Bitwise {
    public static void main(String[] args) {
        int a = 3;
        int b = 3;
        System.out.println("a & b = " + (a & b));
        System.out.println("a | b = " + (a | b));
        System.out.println("a ^ b = " + (a ^ b));
        System.out.println("a << b = " + (a << b));
        System.out.println("a >> b = " + (a >> b));


    }
}